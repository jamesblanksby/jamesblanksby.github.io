'use strict';

/* ////////////////////////////////////////////////////////////////////////////// */
/* ///////////////////////////////////////////////////////////////// DOCUMENT /// */
/* ////////////////////////////////////////////////////////////////////////////// */

/* -------------------------------------------------------------------- READY --- */
$(init);

/* --------------------------------------------------------------------- INIT --- */
function init() {
    // footer
    footer_init();
}


/* ////////////////////////////////////////////////////////////////////////////// */
/* /////////////////////////////////////////////////////////////////// FOOTER /// */
/* ////////////////////////////////////////////////////////////////////////////// */

/* --------------------------------------------------------------------- INIT --- */
function footer_init() {
    // prepare
    header_prepare();
}

/* ------------------------------------------------------------------ PREPARE --- */
function header_prepare() {
    // time
    header_time();

    // code
    header_code();
}

/* --------------------------------------------------------------------- TIME --- */
function header_time() {
    var $time;
    var datetime,
        date,
        year;

    // cache element
    $time = $('footer time');

    // date
    date = new Date();
    datetime = date.toISOString().split('T')[0];
    year = date.getFullYear();

    // date content
    $time.attr('datetime', datetime);
    $time.text(year);
}

/* --------------------------------------------------------------------- CODE --- */
function header_code() {
    var $code;
    var uuid;

    // cache element
    $code = $('footer code');

    // uuid
    uuid = crypto.randomUUID();

    // uuid content
    $code.text(uuid);
}
